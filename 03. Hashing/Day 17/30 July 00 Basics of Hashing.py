    # - Search, Insert, Delete - O(1)

# Hashing is not Usefull For
    # - Finding Closest Value (AVL and RB Tree)
    # - Sorted Data (AVL and RB Tree)
    # - Prefix Searching (Trie)

# Time Complexity in Array
    # - Insert, Delete - O(n)
    # - Array is in Sorted order then Search is O(logn)
    # - Array is in Unorted order then Search is O(n)

    


